package com.java;
public class SingleDimensionalArray {
    public static void main(String[] args) {

        int marks[] = {80, 75, 90, 60, 85};

        System.out.println("Student Marks:");
        for (int i = 0; i < marks.length; i++) {
            System.out.println(marks[i]);
        }
    }
}
